numero_uno = 10 
numero_dos = 20

"""
>   = Mayor 
<   = Menor
>=  = Mayor o igual
<=  = Menor o igual          son utilizados para comprobar valores y siempre retornan un valor booleano.
==  = Igual
!=  = Distinto
"""

resultado = numero_uno > numero_dos
print (resultado)


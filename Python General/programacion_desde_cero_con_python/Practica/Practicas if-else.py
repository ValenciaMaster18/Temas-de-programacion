print ("Verificacion de notas")

Nota_alumnos=int(input("Introduce tu nota, por favor: "))

if Nota_alumnos <5:
	print ("Insuficiente")
elif Nota_alumnos <6:
	print ("Suficiente")
elif Nota_alumnos <7:
	print ("Bien")	
elif Nota_alumnos <8:
	print ("Notable")
elif Nota_alumnos <9:
	print ("Execelente")
else:
	print ("sobresaliente")	